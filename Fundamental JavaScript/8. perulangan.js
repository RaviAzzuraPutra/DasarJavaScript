// // perulangan for
console.log("contoh perulangan for");
const maxLoop = 10;

for (let i = 1; i <= maxLoop; i++) {
    if (i == 5) {
        console.log(i + ". Chelsea");
    } else {
        console.log(i + " . Hello World");
    }
}

console.log("")
console.log("contoh perulangan do while");
let J = 1;
while (J <= maxLoop) {
    if (J == 5) {
        console.log(J + ". Unikom");
    } else {
        console.log(J + " . Keyboard");
    }

    J++;
}

